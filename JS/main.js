document.addEventListener("DOMContentLoaded", () => {
  console.log("صفحة DOM محملة بالكامل")

  const elements = {
    searchBox: document.querySelector("#searchInput"),
    articlesContainer: document.getElementById("articles-container"),
  }

  function displayArticles(articlesToDisplay) {
    if (!elements.articlesContainer) return

    elements.articlesContainer.innerHTML = ""
    articlesToDisplay.forEach((article) => {
      const articleElement = document.createElement("div")
      articleElement.className = "col-lg-4 col-md-6 mb-4"
      articleElement.innerHTML = `
        <div class="card article-card h-100 animate__animated animate__fadeInUp">
          <img src="${article.image}" class="card-img-top lazy" s alt="${article.title}" data-src="${article.image}">
          <div class="card-body d-flex flex-column">
            <h5 class="card-title text-primary fw-bold">${article.title}</h5>
            <p class="card-text flex-grow-1">${article.excerpt}</p>
            <a href="${article.link}" class="btn-read-more mt-auto">اقرأ المزيد</a>
          </div>
        </div>
      `
      elements.articlesContainer.appendChild(articleElement)
    })
  }

  function searchArticles() {
    if (!elements.searchBox) return

    const searchTerm = elements.searchBox.value.toLowerCase()
    const filteredArticles = articles.filter(
      (article) =>
        article.title.toLowerCase().includes(searchTerm) || article.excerpt.toLowerCase().includes(searchTerm),
    )
    displayArticles(filteredArticles)
  }

  function debounce(func, wait) {
    let timeout
    return function (...args) {
      clearTimeout(timeout)
      timeout = setTimeout(() => func.apply(this, args), wait)
    }
  }

  if (elements.searchBox) {
    elements.searchBox.addEventListener("input", debounce(searchArticles, 300))
  }

  // Display articles on page load
  displayArticles(articles)

  // Update current year in footer
  const currentYearElement = document.getElementById("currentYear")
  if (currentYearElement) {
    currentYearElement.textContent = new Date().getFullYear()
  }

  // Reading progress bar
  const progressBar = document.querySelector(".reading-progress-bar")
  if (progressBar) {
    window.addEventListener("scroll", () => {
      const winScroll = document.body.scrollTop || document.documentElement.scrollTop
      const height = document.documentElement.scrollHeight - document.documentElement.clientHeight
      const scrolled = (winScroll / height) * 100
      progressBar.style.width = scrolled + "%"
    })
  }

  // Back to top button
  const backToTopBtn = document.getElementById("backToTop")
  if (backToTopBtn) {
    window.addEventListener("scroll", () => {
      if (window.pageYOffset > 300) {
        backToTopBtn.style.display = "block"
      } else {
        backToTopBtn.style.display = "none"
      }
    })

    backToTopBtn.addEventListener("click", () => {
      window.scrollTo({
        top: 0,
        behavior: "smooth",
      })
    })
  }

  // Load more functionality
  const loadMoreBtn = document.getElementById("loadMoreBtn")
  if (loadMoreBtn) {
    loadMoreBtn.addEventListener("click", function () {
      // Simulate loading more articles
      this.innerHTML = '<i class="fas fa-spinner fa-spin me-2"></i>جاري التحميل...'

      setTimeout(() => {
        this.innerHTML = '<i class="fas fa-plus me-2"></i>تحميل المزيد'
        // Add more articles logic here
      }, 2000)
    })
  }

  // Animate cards on scroll
  const observerOptions = {
    threshold: 0.1,
    rootMargin: "0px 0px -50px 0px",
  }

  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add("animate")
      }
    })
  }, observerOptions)

  // Observe all cards
  document.querySelectorAll(".card").forEach((card) => {
    observer.observe(card)
  })

  // Star rating functionality
  const stars = document.querySelectorAll(".star")
  stars.forEach((star, index) => {
    star.addEventListener("click", () => {
      // Remove selected class from all stars
      stars.forEach((s) => s.classList.remove("selected"))

      // Add selected class to clicked star and all previous stars
      for (let i = 0; i <= index; i++) {
        stars[i].classList.add("selected")
      }

      // Show rating result
      const ratingResult = document.getElementById("ratingResult")
      if (ratingResult) {
        ratingResult.textContent = `شكراً لك! تقييمك: ${index + 1} من 5 نجوم`
        ratingResult.style.display = "block"
      }
    })
  })

  // Copy article functionality
  const copyBtn = document.getElementById("copyArticleBtn")
  if (copyBtn) {
    copyBtn.addEventListener("click", () => {
      const articleContent = document.querySelector(".card-body").innerText

      navigator.clipboard
        .writeText(articleContent)
        .then(() => {
          showCopyAlert("تم نسخ المقال بنجاح!")
        })
        .catch(() => {
          showCopyAlert("فشل في نسخ المقال", "error")
        })
    })
  }

  // Show copy alert
function showCopyAlert(message, type = "success") {
  const alert = document.querySelector(".copy-alert");
  if (alert) {
    alert.textContent = message;
    alert.className = `copy-alert alert ${type === "error" ? "alert-danger" : "alert-success"}`;
    alert.classList.add("show");

    setTimeout(() => {
      alert.classList.remove("show");
      setTimeout(() => {
        alert.style.display = "none";
      }, 500);
    }, 3000);
  }
}



  // Smooth scrolling for anchor links
  document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
    anchor.addEventListener("click", function (e) {
          const href = this.getAttribute("href")
    
    
    if (href === "#") return
      e.preventDefault()
      const target = document.querySelector(this.getAttribute("href"))
      if (target) {
        target.scrollIntoView({
          behavior: "smooth",
          block: "start",
        })
      }
    })
  })

  // Form validation
  const forms = document.querySelectorAll("form")
  forms.forEach((form) => {
    form.addEventListener("submit", (e) => {
      e.preventDefault()

      // Basic validation
      const requiredFields = form.querySelectorAll("[required]")
      let isValid = true

      requiredFields.forEach((field) => {
        if (!field.value.trim()) {
          field.classList.add("is-invalid")
          isValid = false
        } else {
          field.classList.remove("is-invalid")
          field.classList.add("is-valid")
        }
      })

      if (isValid) {
        // Show success message
        const successAlert = document.getElementById("successAlert")
        if (successAlert) {
          successAlert.classList.add("show")
          setTimeout(() => {
            successAlert.classList.remove("show")
          }, 5000)
        }

        // Reset form
        form.reset()
        form.querySelectorAll(".is-valid").forEach((field) => {
          field.classList.remove("is-valid")
        })
      }
    })
  })

  // Initialize tooltips if Bootstrap is loaded
  const bootstrap = window.bootstrap // Declare the bootstrap variable
  if (bootstrap) {
    const tooltipTriggerList = [].slice.call(document.querySelectorAll('[data-bs-toggle="tooltip"]'))
    tooltipTriggerList.map((tooltipTriggerEl) => new bootstrap.Tooltip(tooltipTriggerEl))
  }
})

// Lazy loading for images
if ("IntersectionObserver" in window) {
  const imageObserver = new IntersectionObserver((entries, observer) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        const img = entry.target
        img.src = img.dataset.src
        img.classList.remove("lazy")
        imageObserver.unobserve(img)
      }
    })
  })

  document.querySelectorAll("img[data-src]").forEach((img) => {
    imageObserver.observe(img)
  })
}
